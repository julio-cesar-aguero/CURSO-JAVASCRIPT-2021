/*
    OBJETO MATH(Objeto estatico)
    Es un ubjeto que se utiliza para operaciones matemáticas más especificas
        Propiedades: 
            * Math.e - Math.Pi
        Métodos:
            * Math.abs(x) Valor absoluto de x.
            * Math.ceil(x) redondea hacia arriba.
            * Math.floor(x) redondea hacia abajo.
            * Math.pow(x,y) Devuelve la potencia de 
                x elevado a y.
            * Math.random() Genera un número Pseudoaleatorio entre 0 y 1.
            * Math.round(x) Devuelve el valor de un número redondeado al entero más
                cercano.
            * Math.sign(x) Devuelve el signo de la x, que indica si x es positivo, negativo
                o cero.
            * Math.sqrt(x) Devuelve la raiz cuadrada de x.
*/
console.log("objeto math");
console.log(Math.E);
console.log(Math.PI);
let num = 5.4;
console.log(Math.abs(num));
console.log(Math.ceil(num))
console.log(Math.floor(num))
console.log(Math.pow(2,3))
console.log(Math.round(Math.random()*100));
console.log(Math.sign(0))
console.log(Math.sqrt(10));

